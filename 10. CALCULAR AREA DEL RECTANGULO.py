print("CALCULAR AREA DEL RECTANGULO")
print(" ")

#Entradas
#Base = Anchura
Altura = float(input("Ingrese el valor de la altura : "))
Base = float(input("Ingrese el valor de la base     : "))

#Proceso
#Formula Area = Altura x Base
Area = (Altura * Base)

print(" ")
#Salida
print("El Area del Rectangulo es: ", Area)
