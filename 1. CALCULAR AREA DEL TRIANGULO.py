# Calcular el area de un triangulo
print ("1. CALCULAR AREA DE UN TRIANGULO \n")

#Entrada
base = int(input("ingrese la base del triangulo: "))
altura = int(input("Ingrese la altura del triangulo: "))

print(" ")

#Proceso
area = (base * altura) / 2

#Salida
print("El area del triangulo es: ", area)
