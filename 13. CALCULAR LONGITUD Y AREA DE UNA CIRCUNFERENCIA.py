print("**CALCULAR LONGITUD Y AREA DE UNA CIRCUNFERENCIA**")
print(" ")

#Entradas
r = int(input("Digite el Radio de la Circunferencia: "))
pi = 3.1416

#Proceso
#Formafula para calcular logitud de una circunferencia
# 2.r.pi (2 x r x pi)
L = ((2 * r) * pi)
print(" ")
print(" ")
#Formula para Calcular el Area de una Circunferencia
#Area = pi.r^2
A = (pi * (r ** 2))

#salida

print("La Logitud de la Circunferencia es : ", L)
print(" ")
print("El Area de la Circunferencia es    : ", A)

