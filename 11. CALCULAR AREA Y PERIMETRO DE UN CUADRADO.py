print("**CALCULAR AREA Y PERIMETRO DE UN CUADRADO**")
print(" ")

#Entradas
A = int(input("Digite Valor del un lado del Cuadrado: "))

P = A

print(" ")
print("Calculando Area y Perimetro")

#Proceso 
#Formula para calcular el area (Lado x Lado) ó L² 
Area = (A ** 2)
print(" ")

#Formula para calcular perimmetro (L+L+L+L) ó (L * 4)
Perimetro = (P * 4)


# Salida
print("El Area del cuadrado es      : ", Area)
print(" ")
print("El Perimetro del cuadrado es : ", Perimetro)
